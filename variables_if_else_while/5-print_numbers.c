#include <stdio.h>

/**
 * main - Prints all single-digit numbers of base 10 starting from 0.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
    int i;

    /* Loop through single-digit numbers from 0 to 9 */
    for (i = 0; i <= 9; i++)
    {
        printf("%d\n", i);
    }

    return (0);
}
